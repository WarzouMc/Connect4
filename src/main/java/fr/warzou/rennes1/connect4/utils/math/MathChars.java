package fr.warzou.rennes1.connect4.utils.math;

/**
 * Provides some maths char.
 */
public final class MathChars {

    /**
     * Membership char.
     */
    public static final char IN = '\u2208';
    /**
     * Not membership char
     */
    public static final char NOT_IN = '\u2209';

    /**
     * Include open interval char.
     */
    public static final char OPEN_INTERVAL_INCLUDE = '[';
    /**
     * Exclude open interval char.
     */
    public static final char OPEN_INTERVAL_EXCLUDE = ']';
    /**
     * Include close interval char.
     */
    public static final char CLOSE_INTERVAL_INCLUDE = ']';
    /**
     * Exclude close interval char.
     */
    public static final char CLOSE_INTERVAL_EXCLUDE = '[';

    /**
     * Equal char.
     */
    public static final char EQUAL_TO = '=';
    /**
     * Not equal char.
     */
    public static final char NOT_EQUAL_TO = '\u2260';
}
